package string;

public class ContaintMethod
{
    public static void main(String[] args) {
        String s="What do you know about me";

        System.out.println(s.contains("What"));
        System.out.println(s.contains("about"));
        System.out.println(s.contains("hello"));
        System.out.println(s.contains("me"));
        System.out.println(s.contains("pradip"));
    }
}
