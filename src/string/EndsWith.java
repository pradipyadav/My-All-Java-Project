package string;

public class EndsWith
{
    public static void main(String[] args) {
        String s="My name is pradip";

        System.out.println(s.endsWith("p"));
        System.out.println(s.endsWith("pradip"));
        System.out.println(s.endsWith("Pradip"));
        System.out.println(s.endsWith("is"));
    }
}
